function Braun() {
  var x = document.getElementById("Braunoff");
  var y = document.getElementById("Braunon");
  if (y.style.display === "none") {
      y.style.display = "block";
      x.style.display = "none";
    } else {
      y.style.display = "none";
      x.style.display = "block";
    }
}

